package cli

import (
	"io/ioutil"
	"net/http"
	"os"
	"os/exec"
	"path/filepath"
	"strconv"
	"time"

	"github.com/030/go-utils"
	log "github.com/sirupsen/logrus"
)

func setup() {
	// Start docker nexus
	cmd := exec.Command("bash", "-c", "docker run -d -p 8081:8081 --name nexus sonatype/nexus3:3.16.1")
	stdoutStderr, err := cmd.CombinedOutput()
	if err != nil {
		log.Fatal(err, string(stdoutStderr))
	}

	pingURL := n.URL + pingURI

	// Waitfor ping URL to become available
	for !utils.URLExists(pingURL) {
		log.Info("Nexus not available.")
		time.Sleep(30 * time.Second)
	}

	// Waitfor ping endpoint to return pong
	for !n.pong() {
		log.Info("Nexus Pong not returned yet.")
		time.Sleep(3 * time.Second)
	}

	// Send test artifacts to docker nexus
	for i := 1; i <= 160; i++ {
		n.createArtifactsAndSubmit(strconv.Itoa(i))
	}

	defer cleanupFiles("file*")
}

func shutdown() {
	cmd := exec.Command("bash", "-c", "docker stop nexus && docker rm nexus")
	stdoutStderr, err := cmd.CombinedOutput()
	if err != nil {
		log.Fatal(err, string(stdoutStderr))
	}

	cleanupFiles("download/file*")
}

func (n Nexus3) pong() bool {
	pongAvailable := false

	// Generated by curl-to-Go: https://mholt.github.io/curl-to-go

	req, err := http.NewRequest("GET", "http://localhost:8081/service/metrics/ping", nil)
	if err != nil {
		// handle err
	}
	req.SetBasicAuth(n.User, n.Pass)

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		// handle err
	}
	defer resp.Body.Close()

	//so
	if resp.StatusCode == http.StatusOK {
		bodyBytes, err := ioutil.ReadAll(resp.Body)
		if err != nil {
			log.Fatal(err)
		}
		bodyString := string(bodyBytes)

		if bodyString == "pong\n" {
			pongAvailable = true
		}
	}
	return pongAvailable
}

func (n Nexus3) submitArtifact(f string) {
	cmd := exec.Command("bash", "-c", "curl -u "+n.User+":"+n.Pass+" -X POST \"http://localhost:8081/service/rest/v1/components?repository=maven-releases\" -H  \"accept: application/json\" -H  \"Content-Type: multipart/form-data\" -F \"maven2.asset1=@file"+f+".pom\" -F \"maven2.asset1.extension=pom\" -F \"maven2.asset2=@file"+f+".jar\" -F \"maven2.asset2.extension=jar\"")
	stdoutStderr, err := cmd.CombinedOutput()
	if err != nil {
		log.Fatal(err, string(stdoutStderr))
	}
}

func createPOM(f string) {
	createArtifact("file"+f+".pom", "<project>\n<modelVersion>4.0.0</modelVersion>\n<groupId>file"+f+"</groupId>\n<artifactId>file"+f+"</artifactId>\n<version>1.0.0</version>\n</project>")
}

func createJAR(f string) {
	createArtifact("file"+f+".jar", "some-content")
}

func (n Nexus3) createArtifactsAndSubmit(f string) {
	createPOM(f)
	createJAR(f)
	n.submitArtifact(f)
}

func cleanupFiles(re string) {
	files, err := filepath.Glob(re)
	if err != nil {
		log.Fatal(err)
	}
	if len(files) == 0 {
		log.Fatal("No files to be removed were found")
	}
	for _, f := range files {
		if err := os.Remove(f); err != nil {
			log.Fatal(err)
		}
	}
}

func (n Nexus3) downloadTestArtifact(repository string, f string, version string, extension string) {
	req, err := http.NewRequest("GET", "http://localhost:8081/service/rest/v1/search/assets/download?repository="+repository+"&name="+f+"&version="+version+"&maven.extension="+extension+"", nil)

	if err != nil {
		log.Fatal(err)
	}
	req.SetBasicAuth(n.User, n.Pass)
	req.Header.Set("Accept", "application/json")

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		log.Fatal(err)
	}
	defer resp.Body.Close()

	body, err := ioutil.ReadAll(resp.Body)
	createArtifact("downloaded-"+f+"-"+version+"."+extension, string(body))
}
